            <li>
                <strong>Health:</strong> This is how much health you have to be used when attacking
            </li>
            <li>
                <strong>Energy:</strong> Energy is used to train and complete other actions in the game such as attacking. The more energy you have, the more you'll gain from training.
            </li>
            <li>
                <strong>Nerve:</strong> Nerve allows you to commit crimes, the more nerve you have the better the crimes you'll be able to commit, earning you more money and more exp.
            </li>
            <li>
                <strong>Awake:</strong> Awake is effected by which house you have and also impacts the amount you gain when training your stats in the gym.
            </li>
