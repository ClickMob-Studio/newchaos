        <h3>Crimes</h3>

        <p>
            Crimes are the a core part of the game and they'll help you gain cash and EXP as well as complete missions. Make sure at the bottom of the crimes page you enable auto refills,
            will make criming much faster for you. You need to pay for and enable auto refills everyday.
        </p>

        <hr />
        <h3>Gym</h3>

        <p>
            The gym is where you can train your stats ready for battle. You have the following stats available to train:
        </p>
        <ul>
            <li>Strength - This is your attack power.</li>
            <li>Defense - This is for defending against your opponents Strength.</li>
            <li>Speed - If yours is higher, it gives you the first hit in an attack.</li>
        </ul>

        <p>It's always smart to hold back on training until you can gain a better house and increase your level to get more from your trains.</p>

        <hr />

        <h3>Backalley</h3>

        <p>The Backalley is a great place to earn some EXP, Cash and Points. It takes energy to search in the Backalley and each search you'll either find something or end up in the hospital. </p>
        <p>If your going to commit to spending some time searching the Backalley then make sure you have some 100% Med Cert's available as you'll need them! They can be purchased from the Pharmacy on the City page.</p>

        <hr />
        <h3>Missions</h3>

        <p>
            Missions are the main place you can earn points and are a great source of gaining EXP when you start to become a higher level. Each mission gives you a different task in order for you to gain points and EXP.
            Missions are available to complete every 24-hours.
        </p>

        <p>There are 6 different types of missions to complete:</p>
        <ul>
            <li>Mission Sets: These are missions which require you to complete multiple different actions to complete them</li>
            <li>Kill Missions: These require you to complete attacks on other players in order to complete them.</li>
            <li>Bust Missions: These require you to bust other players or bots out of jail in order to complete them.</li>
            <li>Crime Missions: These require you to complete crimes. (TIP: When completing your missions, run the 1 nerve Crime on 10x multiple to complete the mission as quickly as possible for the most points profit)</li>
            <li>Mug Missions: These require you to successfully mug another player to complete them</li>
            <li>BA Missions: These require you to complete Backalley searches to complete them</li>
        </ul>

